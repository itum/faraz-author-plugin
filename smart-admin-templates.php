// تابع برای افزودن پرامپت‌های پیش‌فرض به تنظیمات
function add_default_prompts_to_settings() {
    $existing_prompts = get_option('smart_admin_saved_prompts', array());
    
    // اگر قبلاً پرامپت‌ها اضافه نشده‌اند
    if (empty($existing_prompts) || !get_option('smart_admin_default_prompts_added')) {
        $default_prompts = get_default_content_prompts();
        
        foreach ($default_prompts as $prompt) {
            $existing_prompts[] = array(
                'prompt' => $prompt['prompt'],
                'model' => $prompt['model'],
                'title' => $prompt['title'],
                'date' => current_time('mysql'),
                'is_template' => true
            );
        }
        
        update_option('smart_admin_saved_prompts', $existing_prompts);
        update_option('smart_admin_default_prompts_added', true);
    }
}

// افزودن پرامپت‌های پیش‌فرض هنگام فعال‌سازی افزونه
add_action('admin_init', 'add_default_prompts_to_settings'); 